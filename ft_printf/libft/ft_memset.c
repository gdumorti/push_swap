#include "libft.h"

void	*ft_memset(void *str, int a, size_t b)
{
	size_t	i;

	i = 0;
	b++;
	while (i < b - 1)
	{
		*((unsigned char *) str + i) = a;
		i++;
	}
	return (str);
}
