#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t					i;
	const unsigned char		*p;

	p = str;
	i = 0;
	while (i < n)
	{
		if (p[i] == (unsigned char)c)
			return ((void *)p + i);
		i++;
	}
	return (NULL);
}
